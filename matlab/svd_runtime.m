% ------------------------------------------------------------------------------

# ------------------------------------------------------------------------------
# -- Clean ---------------------------------------------------------------------
# ------------------------------------------------------------------------------

clear;
clc;

# ------------------------------------------------------------------------------
# -- Main ----------------------------------------------------------------------
# ------------------------------------------------------------------------------

rand('seed',321)

matrixSize = 10:10:1000;
svdRuntime = zeros(size(matrixSize,2),2);

# ----------------------------------------
# -- Runtime -----------------------------
# ----------------------------------------

for i = 1 : size(matrixSize,2)

  svdRuntime(i,1) = matrixSize(i);
  
  for j = 1 : 10
    
    disp(sprintf('%d | Matrix size = %d | Repetition = %d', i, matrixSize(i), j))
    
    randomMatrix = cov(rand(matrixSize(i)));
    tic;
    svd(randomMatrix);
    svdRuntime(i,2) = svdRuntime(i,2) + toc;
    
  end
  
    svdRuntime(i,2) = 0.1 * svdRuntime(i,2);
  
end

%% Clear

clear i
clear j
